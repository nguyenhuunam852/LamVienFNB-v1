<script>
import $ from 'jquery'
import 'owl.carousel/dist/assets/owl.carousel.css'
import 'owl.carousel'

export default {
  mounted() {

    $(document).ready(function () {
      var owl = $('.owl-carousel')
      owl.owlCarousel({
        margin: 10,
        nav: true,
        loop: true,
        responsive: {
          0: {
            items: 1,
          },
          600: {
            items: 2,
          },
          1000: {
            items: 3,
          },
        },
      })
    })

    function animateOnScroll() {

      let windowTop = $(window).scrollTop()
      let windowBottom = $(window).scrollTop() + $(window).height()

      $('.palne-img-area .hidden, .palne-img-area .visible').each(function () {

        let elementTop = $(this).offset().top
        let elementBottom = elementTop + $(this).outerHeight()

        if (elementBottom > windowBottom - 5) {
          let delay = 200
          $(this).css('animation-delay', delay + 'ms')
          $(this).removeClass('hidden').addClass('visible animate__fadeInUp')
        }
      })

      $('#demos .hidden, #demos .visible').each(function (index) {
        let elementTop = $(this).offset().top
        let elementBottom = elementTop + $(this).outerHeight()

        if (elementBottom < windowBottom) {
          let delay = index * 100
          $(this).css('animation-delay', delay + 'ms')
          $(this).removeClass('hidden').addClass('visible animate__fadeInRight')
        }
      })

      $('.section-3 .hidden, .section-3 .visible').each(function (index) {
        let elementTop = $(this).offset().top
        let elementBottom = elementTop + $(this).outerHeight()

        if (elementBottom < windowBottom + 10) {
          let delay = index * 200
          $(this).css('animation-delay', delay + 'ms')
          $(this).removeClass('hidden').addClass('visible animate__fadeInLeft')
        }
      })

      if (windowTop == 0) {
        $('.palne-img-area .visible').removeClass('visible animate__fadeInUp').addClass(
          'hidden')
        $('#demos .hidden, #demos .visible').removeClass('visible animate__fadeInRight').addClass(
          'hidden')
        $('.section-3 .hidden, .section-3 .visible').removeClass('visible animate__fadeInLeft').addClass(
          'hidden')
      }
    }

    $(window).on('scroll', animateOnScroll)

    setTimeout(function () {
      $('body').addClass('loaded')
      $('.loading').fadeOut()

      $('html, body').animate({
        scrollTop: 0,
      })

    }, 2000)
  },
}
</script>
